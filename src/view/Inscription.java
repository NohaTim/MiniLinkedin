    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Utilisateur utilisateur = getParam();
        int res = utilisateurService.inscrir(utilisateur);
        if (res == 1 && !(jTextField1.getText().equals("")) && !(jTextField2.getText().equals("")) && !(jPasswordField1.getText().equals(""))) {
            Session.updateAttribute(utilisateur, "InscriptionUser");
            JOptionPane.showMessageDialog(null, "INSCRIPTION TERMINER", "info", JOptionPane.INFORMATION_MESSAGE);
            new ProfilView().setVisible(true);
            this.setVisible(false);

        } else if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "VEUILLEZ ENTRER UN VOTRE NOM ", "info", JOptionPane.ERROR_MESSAGE);
        } else if (jTextField2.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "VEUILLEZ ENTRER UN VOTRE PRENOM ", "info", JOptionPane.ERROR_MESSAGE);
        } else if (jPasswordField1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "VEUILLEZ ENTRER UN MOT DE PASSE ", "info", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

